//NVS read/write
void esp_nvs_read(char *ssid,char  *pw)
{
	esp_err_t err;
    nvs_handle_t my_handle;
	size_t lenght;
    err = nvs_open("storage", NVS_READWRITE, &my_handle);
    if (err != ESP_OK) {
        ESP_LOGE(GATTS_TAG, "Error (%s) opening NVS handle!\n", esp_err_to_name(err));
		return ;
	}

	lenght = 32;
	err = nvs_get_str(my_handle,"ssid",ssid,&lenght);
    switch (err) {
        case ESP_OK:
            ESP_LOGI(GATTS_TAG,"Done %s \n",ssid);
            break;
        case ESP_ERR_NVS_NOT_FOUND:
            ESP_LOGE(GATTS_TAG,"The value is not initialized yet!\n");
			memcpy(ssid,"xcomp",5);
            break;
        default :
            ESP_LOGE(GATTS_TAG,"Error (%s) reading!\n", esp_err_to_name(err));
    }
	lenght = 64;
	err = nvs_get_str(my_handle,"pw",pw,&lenght);
    switch (err) {
        case ESP_OK:
            ESP_LOGI(GATTS_TAG,"Done %s \n",pw);
            break;
        case ESP_ERR_NVS_NOT_FOUND:
            ESP_LOGE(GATTS_TAG,"The value (%s) is not initialized yet!\n", esp_err_to_name(err));
			memcpy(pw,"1111111111",10);
            break;
        default :
            ESP_LOGE(GATTS_TAG,"Error (%s) reading!\n", esp_err_to_name(err));
    }
    nvs_close(my_handle);
}
void esp_nvs_write(char *ssid,char  *pw)
{
	esp_err_t err;
    nvs_handle_t my_handle;
    err = nvs_open("storage", NVS_READWRITE, &my_handle);

	err = nvs_set_str(my_handle,"ssid",ssid);
    //ESP_LOGI(GATTS_TAG,((err != ESP_OK) ? "Failed!\n" : "Done\n"));
	ESP_ERROR_CHECK(err);

	err = nvs_set_str(my_handle,"pw", pw);
    //ESP_LOGI(GATTS_TAG,((err != ESP_OK) ? "Failed!\n" : "Done\n"));
	ESP_ERROR_CHECK(err);

    err = nvs_commit(my_handle);
    //ESP_LOGI(GATTS_TAG,((err != ESP_OK) ? "Failed!\n" : "Done\n"));
	ESP_ERROR_CHECK(err);
    // Close
    nvs_close(my_handle);
	esp_restart();
}
